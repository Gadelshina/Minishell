#include "minishell.h"

int	ft_env(void)
{
	int	i;

	i = 0;
	while (__environ[i])
	{
		if (ft_strchr(__environ[i], '='))
			printf("%s\n", __environ[i]);
		i++;
	}
	return (0);
}
